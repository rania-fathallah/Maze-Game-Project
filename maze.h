double oldTime = 0; // time of previous frame

/**
 * Initializes the SDL window and renderer.
 */
void init();

/**
 * Renders the game scene, including the player, walls, and mini-map if visible.
 */
void render();

/**
 * Clears the screen to black.
 */
void clearScreen();

/**
 * Draws the player on the screen.
 *
 * @param player The player object to draw.
 */
void drawPlayer(Player player);

/**
 * Handles input events, such as keyboard presses and mouse movements.
 *
 * @param event The SDL event to handle.
 * @param player The player object to update.
 */
void handleInput(SDL_Event &event);

/**
 * Checks if the player is colliding with a wall.
 *
 * @param newX The new x-coordinate of the player.
 * @param newY The new y-coordinate of the player.
 * @return True if there is a collision, false otherwise.
 */
bool isColliding(float newX, float newY);

/**
 * Draws a vertical line on the screen.
 *
 * @param x The x-coordinate of the line.
 * @param drawStart The starting y-coordinate of the line.
 * @param drawEnd The ending y-coordinate of the line.
 * @param color The color of the line.
 */
void drawVerticalLine(int x, int drawStart, int drawEnd, SDL_Color color);

/**
 * Gets the current system time in milliseconds.
 *
 * @return The current system time in milliseconds.
 */
Uint32 getTicks();

/**
 * Draws the mini-map, showing the player's position and the maze layout.
 *
 * @param player The player object.
 */
void drawMiniMap(Player player);

